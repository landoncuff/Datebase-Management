-- An airline is a commercial company offering flights to the public
CREATE TABLE Airlines (
	AirlineID INTEGER PRIMARY KEY IDENTITY(1,1),
	AirlineName VARCHAR(20)
);
GO

-- One airline can fly many types of planes
CREATE TABLE Planes (
	PlaneID INT PRIMARY KEY IDENTITY(100,1),
	Manufacturer VARCHAR(20),
	Model VARCHAR(25),
	PurchaseDate DATE,
	LastServiceDate DATE,
	AirlineID INT REFERENCES Airlines(AirlineID),
	NumberOfSeats INT
);
GO

-- One airport facilitates many flights
CREATE TABLE Airports (
	AirportCode CHAR(3) PRIMARY KEY,
	AirportCity VARCHAR(25),
	AirportState CHAR(2),
	YearOpened INT,
	NumTerminals INT
);
GO

-- Each flight involves many airports
CREATE TABLE Flights (
	FlightID INT PRIMARY KEY IDENTITY(1000,1),
	PlaneID INT REFERENCES Planes(PlaneID),
	AirlineID INT REFERENCES Airlines(AirlineID),
	DepartCode CHAR(3) REFERENCES Airports(AirportCode),
	ArriveCode CHAR(3) REFERENCES Airports(AirportCode),
	DepartDateTime DATETIME,
	ArriveDateTime DATETIME
);
GO

