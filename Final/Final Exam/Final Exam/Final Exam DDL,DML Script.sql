--Create and populate movie genres
CREATE TABLE Genres (
	GenreID	INT PRIMARY KEY,
	GenreName	VARCHAR(15)
);


--Create and populate movie ratings
CREATE TABLE Ratings (
	RatingID	INT PRIMARY KEY,
	RatingCode	VARCHAR(5)
);

--Create and populate directors
CREATE TABLE Directors (
	DirectorID	INT	PRIMARY KEY,
	DirectorName	VARCHAR(35)
);

--Create and populate Actors
CREATE TABLE Actors (
	ActorID	INT	PRIMARY KEY,
	ActorName	VARCHAR(35)
);

-- Creating and populating the Movies table
CREATE TABLE Movies (
	MovieID	INT	PRIMARY KEY,
	MovieTitle	VARCHAR(200),
	IMDbRank	INT,
	IMDbRating	DECIMAL(2,1),
	ReleaseYear	INT,
	GenreID		INT REFERENCES Genres(GenreID),
	DirectorID	INT REFERENCES Directors(DirectorID),
	RatingID	INT REFERENCES Ratings(RatingID),
	USBoxOffice	MONEY
);

-- Creating and populating the MovieActors table
CREATE TABLE MovieActors (
	MovieID	INT	REFERENCES Movies(MovieID),
	ActorID	INT	REFERENCES Actors(ActorID),
	PRIMARY KEY (MovieID, ActorID)
);


